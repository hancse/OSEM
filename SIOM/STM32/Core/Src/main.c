void HAL_I2C_SlaveRxCpltCallback(I2C_HandleTypeDef *hi2c)
{
	if (i2cCommunication == I2C_STATEMACHINE_DATA)
	{
		SIOM_Input.expansionBoardAnalogOutput = SIOM_Input.i2cArray[0];
		SIOM_Input.expansionBoardHeaterOutput = SIOM_Input.i2cArray[1];
//		SIOM_Input.valve_1 = SIOM_Input.i2cArray[2];
//		SIOM_Input.valve_2 = SIOM_Input.i2cArray[3];

#warning "Data swap for Valve 1 and Valve 2 due to mistake in pcb silkscreen >> are swapped on pcb"
		SIOM_Input.valve_1 = SIOM_Input.i2cArray[3];
		SIOM_Input.valve_2 = SIOM_Input.i2cArray[2];

		if (SIOM_Input.expansionBoardAnalogOutput > 100)
		{
			// 0 - 100%
			SIOM_Input.expansionBoardAnalogOutput = 100;
		}

		if (SIOM_Input.expansionBoardHeaterOutput > 1)
		{
			// On/Off
			SIOM_Input.expansionBoardHeaterOutput = 1;
		}

		if (SIOM_Input.valve_1 > 100)
		{
			// 0 - 100%
			SIOM_Input.valve_1 = 100;
		}

		if (SIOM_Input.valve_2 > 100)
		{
			// 0 - 100%
			SIOM_Input.valve_2 = 100;
		}

		systemDebug("New output: \r\n"
				"expansionBoardAnalogOutput: %d\r\n"
				"expansionBoardHeaterOutput: %d\r\n"
				"valve_1: %d\r\n"
				"valve_2: %d\r\n",
				SIOM_Input.expansionBoardAnalogOutput,
				SIOM_Input.expansionBoardHeaterOutput,
				SIOM_Input.valve_1,
				SIOM_Input.valve_2);

		i2cCommunication = I2C_STATEMACHINE_ADDRESS;
	}
	else if (i2cCommunication == I2C_STATEMACHINE_REGISTER &&
			SIOM_Input.i2cArray[0] == 0x00)
	{
		// Read data
		i2cCommunication = I2C_STATEMACHINE_DATA;
		if (HAL_I2C_Slave_Seq_Receive_IT(hi2c, SIOM_Input.i2cArray, 4, I2C_FIRST_AND_NEXT_FRAME) != HAL_OK)
		{
			systemDebug("Register read failed\r\n");
		}
	}
}
