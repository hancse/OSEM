void HAL_I2C_AddrCallback(I2C_HandleTypeDef *hi2c, uint8_t TransferDirection, uint16_t AddrMatchCode)
{
	if (addressChecked == 0)
	{
		addressChecked = 1;
		uint8_t dummy = 0;
		HAL_I2C_Slave_Seq_Receive_IT(hi2c, &dummy, 1, I2C_LAST_FRAME);
		systemDebug("Address check received\r\n");
		return;
	}

	switch (i2cCommunication)
	{
	case I2C_STATEMACHINE_ADDRESS:
		// Read target register
		if (HAL_I2C_Slave_Seq_Receive_IT(hi2c, SIOM_Input.i2cArray, 1, I2C_FIRST_AND_NEXT_FRAME) != HAL_OK)
		{
			systemDebug("Register read failed\r\n");
		}

		if (TransferDirection == I2C_DIRECTION_RECEIVE)
		{
			writeOrRead = 1;
			// Master receive
		}
		else
		{
			writeOrRead = 2;
			// Master write
		}
		i2cCommunication = I2C_STATEMACHINE_REGISTER;
		break;
	case I2C_STATEMACHINE_REGISTER:
		// Register value
		registerAction = SIOM_Input.i2cArray[0];
		if (registerAction == 0x01)
		{
			// Read output
			i2cCommunication = I2C_STATEMACHINE_DATA;
			i2cOutputData[0] = SIOM_Output.temperatureSensor_1;
			i2cOutputData[1] = SIOM_Output.temperatureSensor_2;
			i2cOutputData[2] = SIOM_Output.temperatureSensor_3;
			i2cOutputData[3] = SIOM_Output.temperatureSensor_4;
			i2cOutputData[4] = SIOM_Output.temperatureSensor_5;
			i2cOutputData[5] = SIOM_Output.temperatureSensor_6;
			i2cOutputData[6] = SIOM_Output.temperatureSensor_7;
			i2cOutputData[7] = SIOM_Output.temperatureSensor_8;
			i2cOutputData[8] = (SIOM_Output.flowInput1Frequency & 0xFF00) >> 8;
			i2cOutputData[9] = (SIOM_Output.flowInput1Frequency & 0x00FF);
			i2cOutputData[10] = (SIOM_Output.flowInput2Frequency & 0xFF00) >> 8;
			i2cOutputData[11] = (SIOM_Output.flowInput2Frequency & 0x00FF);

			if(HAL_I2C_Slave_Seq_Transmit_IT(hi2c, i2cOutputData, 12, I2C_NEXT_FRAME) != HAL_OK)
			{
				systemDebug("Data write failed\r\n");
			}
			i2cCommunication = I2C_STATEMACHINE_ADDRESS;
		}
		else if (registerAction == 0x00)
		{
			// Write input
			i2cCommunication = I2C_STATEMACHINE_DATA;
			if (HAL_I2C_Slave_Seq_Receive_IT(hi2c, SIOM_Input.i2cArray, 4, I2C_FIRST_AND_NEXT_FRAME) != HAL_OK)
			{
				systemDebug("Register read failed\r\n");
			}
		}
		break;
	default:
		break;
	}
}

